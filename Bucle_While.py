
"""Condicional WHILE """

x = 0
x2 = 0
x3 = 10
print("Primer ejercicio\n")
while x < 15: #bucle while
       
    print("El valor de x es:", x, "Aun no llega al valor 15\nse le aumentaran 5 extra") #Print para ver cuanto vale x y a que debe de llegar
    x += 5

  
print("\n\nSegundo ejercicio\n")
